   private void  Hasil(){
   switch(pilih){
       case 1:
           jawaban = Bil + Double.parseDouble(txt_bilangan.getText());
           txt_bilangan.setText(Double.toString(jawaban));
           break;
       case 2:
           jawaban = Bil - Double.parseDouble(txt_bilangan.getText());
           txt_bilangan.setText(Double.toString(jawaban));
           break;
       case 3:
           jawaban = Bil * Double.parseDouble(txt_bilangan.getText());
           txt_bilangan.setText(Double.toString(jawaban));
           break;
       case 4:
           jawaban = Bil / Double.parseDouble(txt_bilangan.getText());
           txt_bilangan.setText(Double.toString(jawaban));
           break;
           
   }
   }
