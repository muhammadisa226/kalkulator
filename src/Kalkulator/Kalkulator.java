    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        int length = txt_bilangan.getText().length();
        int angka = txt_bilangan.getText().length()-1;
        String delete;
        if (length > 0){
        StringBuilder back = new StringBuilder(txt_bilangan.getText());
        back.deleteCharAt(angka);
        delete = back.toString();
        txt_bilangan.setText(delete);
        }
    }//GEN-LAST:event_btn_hapusActionPerformed

